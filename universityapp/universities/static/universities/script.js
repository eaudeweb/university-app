function Dark() {
    document.body.style.backgroundColor = 'rgb(41, 41, 61)';
    document.body.style.color = "white"
    document.querySelector('.title').style.color = 'black';
}

function Light() {
    document.body.style.backgroundColor = 'white';
    document.body.style.color = "black"
    document.querySelector('.title').style.color = 'black';
}